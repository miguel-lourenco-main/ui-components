/** Adds two numbers. */
export function dummyAdd(a: number, b: number): number {
  return a + b;
}

/** Subtracts second number from first. */
export function dummySubtract(a: number, b: number): number {
  return a - b;
}

/** Multiplies two numbers. */
export function dummyMultiply(a: number, b: number): number {
  return a * b;
}

/** Divides first number by second. */
export function dummyDivide(a: number, b: number): number {
  return a / b;
}

/** Returns the remainder of division. */
export function dummyModulo(a: number, b: number): number {
  return a % b;
}

/** Returns the power of base raised to exponent. */
export function dummyPower(base: number, exponent: number): number {
  return Math.pow(base, exponent);
}

/** Returns the absolute value of a number. */
export function dummyAbs(value: number): number {
  return Math.abs(value);
}

/** Returns the maximum of two numbers. */
export function dummyMax(a: number, b: number): number {
  return Math.max(a, b);
}

/** Returns the minimum of two numbers. */
export function dummyMin(a: number, b: number): number {
  return Math.min(a, b);
}

/** Rounds a number to the nearest integer. */
export function dummyRound(value: number): number {
  return Math.round(value);
}

/** Rounds a number down to the nearest integer. */
export function dummyFloor(value: number): number {
  return Math.floor(value);
}

/** Rounds a number up to the nearest integer. */
export function dummyCeil(value: number): number {
  return Math.ceil(value);
}

/** Increments a number by 1. */
export function dummyIncrement(value: number): number {
  return value + 1;
}

/** Decrements a number by 1. */
export function dummyDecrement(value: number): number {
  return value - 1;
}

/** Negates a number. */
export function dummyNegate(value: number): number {
  return -value;
}
